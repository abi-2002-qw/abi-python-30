# Solicita la calificación de un estudiante y determina si está aprobado (mayor o igual a 7) o reprobado.
#Ejemplo: Entrada: 6.5 → Salida: 
#
calificacion = float(input("ingresa la calificacion: "))

# aqui vamos a ver si el o la estudiente paso o se quedo de año 
if calificacion >= 7:
    print("aprobado")
else: ##-7 seria -6.9 reprueba 
    print("reprobado")
